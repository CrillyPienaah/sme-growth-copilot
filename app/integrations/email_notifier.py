import os
from typing import Optional, List
from ..schemas import GrowthPlan

class EmailNotifier:
    """Sends growth plan notifications via email"""
    
    def __init__(self):
        self.api_key = os.getenv("SENDGRID_API_KEY")
        self.from_email = os.getenv("EMAIL_FROM", "noreply@growthcopilot.com")
        self.enabled = os.getenv("EMAIL_NOTIFICATIONS_ENABLED", "false").lower() == "true"
        self.test_mode = os.getenv("EMAIL_TEST_MODE", "true").lower() == "true"
        
        # Only import SendGrid if not in test mode
        if not self.test_mode and self.api_key:
            try:
                from sendgrid import SendGridAPIClient
                from sendgrid.helpers.mail import Mail
                self.client = SendGridAPIClient(self.api_key)
                self.Mail = Mail
            except ImportError:
                print("‚ö†Ô∏è sendgrid not installed, using test mode")
                self.test_mode = True
        else:
            self.client = None
    
    def send_plan_email(
        self, 
        plan: GrowthPlan, 
        trace_id: str,
        to_emails: List[str]
    ) -> bool:
        """
        Send a growth plan via email
        
        Args:
            plan: The generated growth plan
            trace_id: Unique trace ID for this plan
            to_emails: List of recipient email addresses
            
        Returns:
            True if sent successfully, False otherwise
        """
        if not self.enabled:
            return False
        
        if not to_emails:
            print("‚ö†Ô∏è No recipient emails provided")
            return False
        
        # Build email content
        subject = f"üöÄ New Growth Plan: {plan.business_profile.name}"
        html_content = self._build_html_email(plan, trace_id)
        text_content = self._build_text_email(plan, trace_id)
        
        # Test mode - just print to console
        if self.test_mode:
            print("\n" + "="*60)
            print("üìß EMAIL NOTIFICATION (TEST MODE)")
            print("="*60)
            print(f"From: {self.from_email}")
            print(f"To: {', '.join(to_emails)}")
            print(f"Subject: {subject}")
            print("\n--- EMAIL CONTENT (Text Version) ---")
            print(text_content)
            print("="*60)
            print("‚úÖ Email sent (test mode)\n")
            return True
        
        # Real mode - send via SendGrid
        try:
            for to_email in to_emails:
                message = self.Mail(
                    from_email=self.from_email,
                    to_emails=to_email,
                    subject=subject,
                    html_content=html_content,
                    plain_text_content=text_content
                )
                
                response = self.client.send(message)
                
                if response.status_code in [200, 202]:
                    print(f"‚úÖ Email sent to {to_email} for trace {trace_id}")
                else:
                    print(f"‚ö†Ô∏è Email failed to {to_email}: {response.status_code}")
                    return False
            
            return True
                
        except Exception as e:
            print(f"‚ùå Email error: {e}")
            return False
    
    def _build_text_email(self, plan: GrowthPlan, trace_id: str) -> str:
        """Build plain text email content"""
        
        # Calculate revenue opportunity
        revenue_opp = plan.kpis.revenue * (1 / (1 - plan.funnel_insight.drop_rate) - 1)
        
        text = f"""
Growth Plan for {plan.business_profile.name}
{'='*60}

BUSINESS INFO
Industry: {plan.business_profile.industry}
Region: {plan.business_profile.region}
Goal: {plan.goal.objective}
Timeframe: {plan.goal.horizon_weeks} weeks

FUNNEL ANALYSIS
Bottleneck: {plan.funnel_insight.from_step} ‚Üí {plan.funnel_insight.to_step}
Drop Rate: {plan.funnel_insight.drop_rate*100:.1f}%
Revenue Opportunity: ${revenue_opp:,.2f}

RECOMMENDED EXPERIMENT
Name: {plan.chosen_experiment.experiment.name}
Channel: {plan.chosen_experiment.experiment.channel}
Priority Score: {plan.chosen_experiment.priority_score:.1f}
  - Impact: {plan.chosen_experiment.impact}
  - Confidence: {plan.chosen_experiment.confidence}
  - Effort: {plan.chosen_experiment.effort}

Hypothesis: {plan.chosen_experiment.experiment.hypothesis}

ALL EXPERIMENTS EVALUATED ({len(plan.experiments)}):
"""
        
        for i, exp in enumerate(plan.experiments[:5], 1):
            text += f"{i}. {exp.experiment.name} (Score: {exp.priority_score:.1f})\n"
        
        if plan.llm_strategy_commentary:
            text += f"\nAI STRATEGY COMMENTARY:\n{plan.llm_strategy_commentary[:300]}...\n"
        
        text += f"\nTrace ID: {trace_id}\n"
        text += f"Generated by SME Growth Co-Pilot\n"
        text += "="*60
        
        return text
    
